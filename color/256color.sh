#!/bin/bash
# 优化版256色终端颜色表生成器
# 功能：显示0-255所有颜色代码的背景色块，并附上对应的前景色转义码
# 每个颜色块显示：背景色块 + 空格 + 前景色转义码文本（背景黑）
# 基本色/216色/灰度色：每行显示3个颜色单元
# 常用颜色示例：每行显示1个，颜色单元后另起一行显示描述

# 根据背景色亮度选择黑色或白色文字 (基于ITU-R BT.601亮度公式)
# 参数：颜色代码（0-255）
# 输出：前景色代码（0或15）
choose_fg() {
    local bg=$1
    if [[ $bg -lt 16 ]]; then
        # 基本色/亮色：0-6较暗用白字，7-15较亮用黑字（7白色本身用黑字）
        [[ $bg -lt 7 || $bg -eq 8 ]] && echo 15 || echo 0
    elif [[ $bg -lt 232 ]]; then
        # 216色：计算RGB值估算亮度 (0-5)
        local r=$(( (bg-16)/36 ))
        local g=$(( ((bg-16)%36)/6 ))
        local b=$(( (bg-16)%6 ))
        # 亮度估算：每个分量映射到0-255范围后取平均
        local bright=$(( (r*42 + g*42 + b*42) / 3 ))  # 42 ≈ 255/6
        [[ $bright -lt 128 ]] && echo 15 || echo 0
    else
        # 灰度色：232-255，共24级
        local gray=$(( bg-232 ))
        [[ $gray -lt 12 ]] && echo 15 || echo 0
    fi
}

# 打印单个颜色单元：背景色块 + 空格 + 前景色转义码文本（背景黑）
# 参数：颜色代码
print_color_cell() {
    local code=$1
    local fg=$(choose_fg $code)          # 背景色块的前景色（保证可读）
    # 背景色块（5字符宽度）
    printf "\033[48;5;%dm\033[38;5;%dm%5s\033[0m" "$code" "$fg" "$code"
    printf " "                             # 空格分隔
    # 转义码文本，背景黑，前景使用代码颜色
    printf "\033[48;5;0m\033[38;5;%dm\\\\033[38;5;%dm\033[0m" "$code" "$code"
}

# 打印标题
print_title() {
    echo -e "\n\033[1;36m=== $1 ===\033[0m"
}

# 主程序
clear
print_title "256色终端颜色预览 (带转义码)"
echo -e "每个单元：背景色块 | 空格 | 前景色转义码文本（背景黑）"
